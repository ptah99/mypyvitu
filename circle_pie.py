import turtle
turtle.title("circle pie")
t = turtle.Turtle()
t.width(5)
t.up()
t.goto(0, -160)
t.down()
t.begin_fill()
t.fillcolor("yellow")
t.circle(160)
t.end_fill()
t.up()
t.home()
t.down()
t.begin_fill()
t.fillcolor("red")
t.circle(80, 120)
t.up()
t.home()
t.down()
t.setheading(90)
t.circle(80, 120)
t.up()
t.home()
t.down()
t.setheading(180)
t.circle(80, 120)
t.up()
t.home()
t.down()
t.setheading(270)
t.circle(80, 120)
t.up()
t.home()
t.end_fill()

turtle.mainloop()
